
package lec02;

public class Test08 {
	public static void main(String[] args) {
		// 형변환 활용..
		// 문자 'k'의 아스키 코드를 화면에 출력하시오
		int kCode = 'k';
		System.out.println(kCode);

		// 변수는 한번만 사용되면 사용하지 않도록 변경한다.
		System.out.println((int)'k');
		
		int val = 100;
		System.out.println(val);

		System.out.println(100);
	}
}
