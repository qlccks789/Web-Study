// 증감 연산자 : ++, --
package lec02;

public class Test11 {
	public static void main(String[] args) {
		int a = 5;
		System.out.println(++a);  // 6
		System.out.println(a--);	// 6	 
		System.out.println(a);		// 5
		System.out.println(a++);	// 5
		System.out.println(--a);    // 5                      
	}
}










