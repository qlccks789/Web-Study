
// 변수의 선언과 초기화

// 변수 : 하나의 값을 담을 수 있는 공간
package lec02;

public class Test05 {
	public static void main(String[] args) {
		// 홍길동, 24 라는 값을 담을 수 있는 공간을 선언한다.
		// 공간(변수) - 필수요구항목 : 이름, 자료형
		// 자료형 이름;  ->  선언
		// 이름 = 값; ->  초기화
		int age;
		age = 10;
		System.out.println(age);
		
		// name cannot be resolved to a variable
//		System.out.println(name);  // 변수는 선언하기 전에 사용이 불가능
		
	}
}












