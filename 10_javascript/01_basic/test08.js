"use strict"

var num1 = 10;
console.log("number : ", num1);

var num2 = 10.2;
console.log("number : ", num2);

var msg = "hi";
console.log("string : ", msg);

var bool = true;
console.log("boolean : ", bool);

var nullVal = null;
console.log("null : ", nullVal);

var undefinedVal;
console.log("undefined : ", undefinedVal);

var objVal = {};
console.log("object : ", objVal);






