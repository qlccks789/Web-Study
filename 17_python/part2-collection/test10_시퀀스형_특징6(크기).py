"""
 시퀀스 자료형의 특징
 - 크기정보 : len()을 이용하여 크기를 얻을 수 있다.
 - len(자료)
"""
my_str = "Hello Python"
my_list = ['H', 'e', 'l', 'l', 'o', ' ', 'P', 'y', 't', 'h', 'o', 'n']
my_tuple = ('H', 'e', 'l', 'l', 'o', ' ', 'P', 'y', 't', 'h', 'o', 'n')

print(len(my_str))     # 12
print(len(my_list))    # 12
print(len(my_tuple))   # 12
